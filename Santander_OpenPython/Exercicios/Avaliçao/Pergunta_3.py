"""Qual estrutura de dados em Python é usada para armazenar uma coleção ordenada e mutável de elementos?"""
from Santander_OpenPython.Exercicios.Avaliçao.Pergunta_1 import resultado


# Lista
# - Mutável
# - Ordenada
# - Permite itens repetidos
# - Acessa por índice
# - Símbolo: [ ]
# Exemplo:
lista = ["a", "b"]

# Tupla
# - Imutável
# - Ordenada
# - Permite itens repetidos
# - Acessa por índice
# - Símbolo: ( )
# Exemplo:
tupla = ("a", "b")

# Set (Conjunto)
# - Mutável
# - Não ordenado
# - NÃO permite itens repetidos
# - Não acessa por índice
# - Símbolo: { }
# Exemplo:
conjunto = {"a", "b"}

# Dicionário (Dict)
# - Mutável
# - Ordenado (desde Python 3.7)
# - Chave-valor
# - Cada chave é única
# - Símbolo: {chave: valor}
# Exemplo:
dicionario = {"a": 1, "b": 2}

